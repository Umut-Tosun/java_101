package JavayaBaslangic;


public class KacisKarakterleri {
    public static void main(String[] args) {
        System.out.println("***\nJAVA 101 PATİKASI\nBaşlıyoruz\n5.04.2022\n***"); // /n Komutu bir satır alta geçer .
        System.out.println("***\tJAVA 101 PATİKASI\tBaşlıyoruz\t5.04.2022\t***"); // /t Komutu bir tab boşlugu bırakır .
        System.out.println("2020399\rUMUT TOSUN"); // /r Komutu yeni bir satır gibi öncesini listelemez .
        System.out.println("Ali \"okula\" gitti "); // \" ile çift tırnak kullanabiliriz .
        System.out.println("5\\6"); // \\ egik çizgi ekler.

    }
}
